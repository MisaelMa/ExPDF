  @doc "Get EC block structure for version and level."
  def ec_info(version, level) do
    case Map.get(@ec_table, {version, level}) do
      nil -> {:error, :unsupported_version}
      {total, ec_per_block, g1_blocks, g1_data, g2_blocks, g2_data} ->
        {:ok, %{
          total_codewords: total,
          ec_per_block: ec_per_block,
          group1: %{blocks: g1_blocks, data_codewords: g1_data},
          group2: %{blocks: g2_blocks, data_codewords: g2_data}
        }}
    end
  end

  @doc "Total data codewords capacity for version and EC level."
  def data_capacity(version, level) do
    case Map.get(@ec_table, {version, level}) do
      nil -> 0
      {_total, _ec_per, g1_b, g1_d, g2_b, g2_d} ->
        g1_b * g1_d + g2_b * g2_d
    end
  end

  @doc "QR code size in modules for a given version."
  def size(version), do: 17 + version * 4

  @doc "Find the smallest version that can hold `byte_count` bytes at `level`."
  def min_version(byte_count, level) do
    Enum.find(1..20, fn v ->
      cc_bits = if v <= 9, do: 8, else: 16
      overhead_bits = 4 + cc_bits
      available_bits = data_capacity(v, level) * 8
      available_bits >= overhead_bits + byte_count * 8
    end) || {:error, :data_too_long}
  end

  @doc "Alignment pattern center coordinates for a version."
  def alignment_positions(version), do: Map.get(@alignment_positions, version, [])

  @doc "Get the 15-bit format information value."
  def format_info(level, mask), do: Map.fetch!(@format_info, {level, mask})

  @doc "Character count indicator bit length for byte mode."
  def char_count_bits(version) when version <= 9, do: 8
  def char_count_bits(_version), do: 16
end
